public class LibraryVo {
    public String library_Number;
    public String library_Name;
    public String library_Location;
    public String library_Introduction;
    public String library_Service_Content;
    public String library_Collection_type;
	public String getLibrary_Number() {
		return library_Number;
	}
	public void setLibrary_Number(String library_Number) {
		this.library_Number = library_Number;
	}
	public String getLibrary_Name() {
		return library_Name;
	}
	public void setLibrary_Name(String library_Name) {
		this.library_Name = library_Name;
	}
	public String getLibrary_Location() {
		return library_Location;
	}
	public void setLibrary_Location(String library_Location) {
		this.library_Location = library_Location;
	}
	public String getLibrary_Introduction() {
		return library_Introduction;
	}
	public void setLibrary_Introduction(String library_Introduction) {
		this.library_Introduction = library_Introduction;
	}
	public String getLibrary_Service_Content() {
		return library_Service_Content;
	}
	public void setLibrary_Service_Content(String library_Service_Content) {
		this.library_Service_Content = library_Service_Content;
	}
	public String getLibrary_Collection_type() {
		return library_Collection_type;
	}
	public void setLibrary_Collection_type(String library_Collection_type) {
		this.library_Collection_type = library_Collection_type;
	}
    
}
